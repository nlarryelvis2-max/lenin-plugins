#!/usr/bin/env python3
"""
verify_gate — forcing-член против цикла №1 (верификация: факт≠гипотеза).
Самый рецидивный цикл срывов (≥7 заходов). trust.py был движком,
но не вызывался автоматически. Этот хук поднимает пол V(t) (error_cycles_ode.md):
когда промпт просит анализ/факты/отчёт/«кто-сколько» — ПРИНУДИТЕЛЬНО впрыскивает
дисциплину штампа ✅/🟡/⚠️/❌ и напоминание, что agent-report = гипотеза до сверки.

UserPromptSubmit hook. Парный к slow_read_gate (чтение) и phil_lens (углы).
"""
import sys, json, re

# Маркеры запроса, несущего фактическую нагрузку (где врать дороже всего)
CLAIM_MARKERS = [
    "проанализируй", "анализ", "отчёт", "отчет", "досье", "аудит", "сколько",
    "кто такой", "кто это", "это факт", "правда ли", "точно ли", "проверь",
    "сверь", "выясни", "найди связ", "карта связ", "профиль", "дашборд",
    "сделай вывод", "резюмируй данные", "цифр", "статистик", "посчитай",
]

def main():
    try:
        data = json.load(sys.stdin)
    except Exception:
        return
    prompt = (data.get("prompt") or "")
    low = prompt.lower()
    words = len(prompt.split())

    has_claim = any(m in low for m in CLAIM_MARKERS)
    # числа в промпте = вероятна работа с фактами
    has_numbers = bool(re.search(r'\d{2,}', prompt))

    if not (has_claim or (has_numbers and words >= 20)):
        return  # разговорный/мнение/креатив — не гейтим

    directive = (
        "── ✅ verify gate (forcing-член, цикл №1: факт≠гипотеза) ──\n"
        "Запрос несёт фактическую нагрузку. Перед тем как ЛЮБОЙ claim выдать за факт:\n"
        "• Штампуй ключевые утверждения: ✅ verified (≥3 независимых сигнала) · 🟡 подтверждено (2) ·\n"
        "  ⚠️ ГИПОТЕЗА (1) · ❓ без основания (0) · ❌ опровергнуто. (trust-discipline / trust.py)\n"
        "• Agent-report, карточка, один документ, keyword-счёт = ГИПОТЕЗА до сверки, не факт.\n"
        "• Сцепленные источники (со-работники, супруги, один родитель-источник) = ~1 голос, не N.\n"
        "• Перед закреплением вывода — ПОПЫТАЙСЯ его опровергнуть (inversion). Не нашёл контрпример — усиль.\n"
        "• Не выдавай эвристику (счёт слов, recency) за строгость (граф, KL, сверка по файлу).\n"
        "При ⚠️/❓ — оставить как есть в финале, не сползать в «ну в целом наверное так»."
    )
    print(directive)

if __name__ == "__main__":
    main()
